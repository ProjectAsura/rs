#include <cstring>


namespace rs {

///////////////////////////////////////////////////////////////////////////////
// PassTag class
///////////////////////////////////////////////////////////////////////////////

//-----------------------------------------------------------------------------
//      コピーコンストラクタです.
//-----------------------------------------------------------------------------
Tag::Tag(const char* value)
{ strcpy_s(m_Tags, value); }

//-----------------------------------------------------------------------------
//      文字列数を取得します.
//-----------------------------------------------------------------------------
size_t Tag::size() const
{ return strnlen_s(m_Tags, MAX_TAG_COUNT); }

//-----------------------------------------------------------------------------
//      文字列を取得します.
//-----------------------------------------------------------------------------
const char* Tag::c_str() const
{ return m_Tags; }

//-----------------------------------------------------------------------------
//      代入演算子です.
//-----------------------------------------------------------------------------
Tag& Tag::operator = (const Tag& value)
{
    strcpy_s(m_Tags, value.m_Tags);
    return *this;
}

} // namespace rs
